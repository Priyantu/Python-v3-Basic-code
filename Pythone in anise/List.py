#List কোন আইটেম Stor করার জন্য
subject = ["C","c++","Java","Python","Android","OS","TOC"]
print(subject) # পুরা মান দেখার জন্য
print(subject[0]) # [0] তম ইনডেক্স এর মান দেখার জন্য
print(subject[2:]) # [2] তম ইন ডেক্স এর পর এর মান দেখার জন্য
print(subject[-1]) # উল্টা দেখার জন্য
print("Python" in subject)#আইটেম আছে কি নাই তা দেখার জন্য 
print("Aco" not in subject)#কোন কিছু নাই তা বলে দেওয়ার জন্য
print(subject + ["Aco",21])#কোন কিছু add করার জন্য
print(subject * 3)#বার বার প্রিন্ট করার জন্য