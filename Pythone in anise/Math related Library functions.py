from math import*   #from math import* লাইব্রেরি ফাংশনে রয়েছে

print(max(20,3))  # max = সব থেকে বড় সংখ্যা খুজতে
print(min(20,3))  # min = সব থেকে ছোট সংখ্যা খুজতে
print(abs(-5))    # abs = - মান কে + এ পরিনত করতে
print(pow(2,3))   # pow = কোন কিছুর ওপর পাওয়ার বসাতে
print(sqrt(5))    # sqrt = রুট এর জন্য
print(round(5.8)) # round = কাছাকাছি পূর্ন সংখ্যা (প্রায় মান)
print(floor(3.7)) # floor = নিচের দিকে পূর্ন সংখ্যা
print(ceil(3.7)) # ceil = ওপরের দিকে পূর্ন সংখ্যা
